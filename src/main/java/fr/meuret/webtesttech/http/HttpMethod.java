package fr.meuret.webtesttech.http;

/**
 * HTTP method according to the RFC7231.
 *
 * @author Jerome
 * @see <a href="https://tools.ietf.org/html/rfc7231#section-4.3">https://tools.ietf.org/html/rfc7231#section-4.3</a>
 */
public enum HttpMethod {


    CONNECT,
    DELETE,
    GET,
    HEAD,
    OPTIONS,
    POST,
    PUT,
    TRACE,


}
